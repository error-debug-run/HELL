# N-GRAM OPTIMIZED DATASET
# ========================
# Intent split:
#   close_app — graceful close: close, exit, quit, shut down, terminate,
#               force close, end, stop <app>
#   kill_app  — hard kill: kill <app> (force-terminates the process)
#
# Design principles:
# 1. ANCHOR GRAMS — each intent has high-signal n-grams that repeat
#    consistently. "kill <app>" exclusively maps to kill_app.
#    "close/exit/quit/stop <app>" exclusively maps to close_app.
# 2. VERB CONTRAST — same app names appear under both kill_app and close_app
#    with different verbs, forcing the model to weight the verb gram.
# 3. UNIGRAM ANCHORS — "kill" alone is near-exclusive to kill_app.
# 4. LENGTH SPREAD — 1-word through 5-word phrases for all n-gram sizes.

